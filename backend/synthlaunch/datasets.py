_UPLOADS: dict[str, dict] = {}


def _load_csv(path_or_buf) -> pd.DataFrame:
    df = pd.read_csv(path_or_buf, dtype={"date": str, "unit_id": str})
    df["date"] = df["date"].astype(str)
    return df


def list_datasets() -> list[dict]:
    out = []
    for ds_id, meta in BUNDLED.items():
        out.append({"id": ds_id, "kind": "bundled", **{k: v for k, v in meta.items() if k != "file"}})
    for ds_id, rec in _UPLOADS.items():
        out.append({"id": ds_id, "kind": "upload", **rec["meta"]})
    return out


def get_dataframe(ds_id: str) -> pd.DataFrame:
    if ds_id in BUNDLED:
        return _load_csv(DATA_DIR / BUNDLED[ds_id]["file"])
    if ds_id in _UPLOADS:
        return _UPLOADS[ds_id]["df"].copy()
    raise KeyError(ds_id)


def get_meta(ds_id: str) -> dict:
    if ds_id in BUNDLED:
        return {"id": ds_id, "kind": "bundled", **BUNDLED[ds_id]}
    if ds_id in _UPLOADS:
        return {"id": ds_id, "kind": "upload", **_UPLOADS[ds_id]["meta"]}
    raise KeyError(ds_id)


def register_upload(raw_csv: bytes, filename: str) -> tuple[str, pd.DataFrame]:
    df = _load_csv(io.BytesIO(raw_csv))
    ds_id = f"upload-{uuid.uuid4().hex[:8]}"
    units = sorted(df["unit_id"].astype(str).unique().tolist()) if "unit_id" in df else []
    dates = sorted(df["date"].astype(str).unique().tolist()) if "date" in df else []
    meta = {
        "title": filename,
        "treated": units[0] if units else None,
        "intervention": dates[len(dates) // 2] if dates else None,
        "metric_label": "metric",
        "description": f"Uploaded file ({len(df)} rows).",
    }
    _UPLOADS[ds_id] = {"df": df, "meta": meta}
    return ds_id, df
